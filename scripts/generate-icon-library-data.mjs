import fs from 'node:fs';
import zlib from 'node:zlib';
import { icons as simpleIconsData } from '@iconify-json/simple-icons';
import { icons as deviconData } from '@iconify-json/devicon';

const OUTPUT_PATH = 'src/generated/IconLibraryData.ts';

function titleFromSlug(slug) {
	return slug
		.replace(/[-_]+/g, ' ')
		.replace(/\b\w/g, char => char.toUpperCase())
		.replace(/\bJs\b/g, 'JS')
		.replace(/\bCss\b/g, 'CSS')
		.replace(/\bHtml\b/g, 'HTML')
		.replace(/\bApi\b/g, 'API')
		.replace(/\bUi\b/g, 'UI')
		.replace(/\bOs\b/g, 'OS')
		.replace(/\bDb\b/g, 'DB')
		.trim();
}

function forceCurrentColor(svgBody) {
	return svgBody
		.replace(/\s(fill|stroke)="(?!none\b|currentColor\b)[^"]*"/gi, ' $1="currentColor"')
		.replace(/(fill|stroke)\s*:\s*(?!none\b|currentColor\b)[^;"']+/gi, '$1:currentColor');
}

function buildLibraryIcons({ data, idPrefix, label, includeIcon }) {
	return Object.keys(data.icons)
		.filter(includeIcon)
		.sort((a, b) => a.localeCompare(b))
		.map(id => {
			const icon = data.icons[id];
			if (!icon?.body) return null;

			const left = icon.left ?? 0;
			const top = icon.top ?? 0;
			const width = icon.width ?? data.width ?? 16;
			const height = icon.height ?? data.height ?? 16;
			const defaultWidth = data.width ?? 16;
			const defaultHeight = data.height ?? 16;
			const dimensions = left || top || width !== defaultWidth || height !== defaultHeight
				? [left, top, width, height]
				: 0;

			return [
				`${idPrefix}-${id}`,
				`${label}: ${titleFromSlug(id)}`,
				forceCurrentColor(icon.body),
				dimensions,
			];
		})
		.filter(Boolean);
}

const iconData = [
	...buildLibraryIcons({
		data: simpleIconsData,
		idPrefix: 'simple',
		label: 'Simple Icons',
		includeIcon: () => true,
	}),
	...buildLibraryIcons({
		data: deviconData,
		idPrefix: 'devicon',
		label: 'Devicon',
		// Wordmark variants make the bundled plugin too large for the 5 MB sync limit.
		includeIcon: id => !id.includes('wordmark'),
	}),
];

const json = JSON.stringify(iconData);
const compressed = zlib.deflateRawSync(json, { level: 9 }).toString('base64');

fs.mkdirSync('src/generated', { recursive: true });
fs.writeFileSync(
	OUTPUT_PATH,
	`// This file is generated by scripts/generate-icon-library-data.mjs. Do not edit manually.\n` +
	`export const COMPRESSED_ICON_LIBRARY_DATA = '${compressed}';\n`,
	'utf8',
);

console.log(`Generated ${OUTPUT_PATH} with ${iconData.length} icons (${json.length} bytes raw, ${compressed.length} bytes base64 deflate).`);
